def main():
    """메인 함수"""
    parser = argparse.ArgumentParser(
        description='GitHub Profile Analyzer - Report Export',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
예제:
  %(prog)s result.json --markdown report.md
  %(prog)s result.json --csv repos.csv
  %(prog)s result.json --html report.html
  %(prog)s result.json --all output_prefix
        """
    )
    
    parser.add_argument(
        'json_file',
        type=str,
        help='분석 결과 JSON 파일'
    )
    
    parser.add_argument(
        '--markdown', '-md',
        type=str,
        help='Markdown 파일로 내보내기'
    )
    
    parser.add_argument(
        '--csv',
        type=str,
        help='CSV 파일로 내보내기'
    )
    
    parser.add_argument(
        '--html',
        type=str,
        help='HTML 파일로 내보내기'
    )
    
    parser.add_argument(
        '--all',
        type=str,
        help='모든 형식으로 내보내기 (접두어 지정)'
    )
    
    args = parser.parse_args()
    
    # JSON 파일 로드
    try:
        with open(args.json_file, 'r', encoding='utf-8') as f:
            data = json.load(f)
        print(f"✅ 파일 로드: {args.json_file}\n")
    except Exception as e:
        print(f"❌ 파일 로드 오류: {e}")
        return 1
    
    username = data.get('username', 'unknown')
    
    # 내보내기 실행
    if args.all:
        prefix = args.all
        export_to_markdown(data, f"{prefix}_{username}_report.md")
        export_to_csv(data, f"{prefix}_{username}_repos.csv")
        export_to_html(data, f"{prefix}_{username}_report.html")
    else:
        if args.markdown:
            export_to_markdown(data, args.markdown)
        
        if args.csv:
            export_to_csv(data, args.csv)
        
        if args.html:
            export_to_html(data, args.html)
        
        if not (args.markdown or args.csv or args.html):
            print("❌ 내보낼 형식을 지정하세요 (--markdown, --csv, --html, 또는 --all)")
            return 1
    
    print("\n✅ 내보내기 완료!")
    return 0


if __name__ == "__main__":
    sys.exit(main())
