#!/usr/bin/env python3
"""
GitHub Profile Analyzer - Report Export
분석 결과를 다양한 형식으로 내보내기
"""

import json
import argparse
import sys
from datetime import datetime
from typing import Dict, Any


def export_to_markdown(data: Dict[str, Any], output_file: str):
    """Markdown 형식으로 내보내기"""
    username = data.get('username', 'Unknown')
    repos = data.get('repositories', [])
    stats = data.get('statistics', {})
    
    md_content = f"""# GitHub Profile Analysis Report

**사용자**: {username}  
**생성일**: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}

---

## 📊 종합 통계

| 항목 | 값 |
|------|-----|
| 총 리포지토리 수 | {stats.get('total_repositories', 0)} |
| 총 커밋 수 | {stats.get('total_commits', 0):,} |
| 총 스타 수 | {stats.get('total_stars', 0):,} |
| 총 포크 수 | {stats.get('total_forks', 0):,} |
| 총 코드량 | {stats.get('total_size_mb', 0):.2f} MB |
| 평균 커밋/리포 | {stats.get('avg_commits_per_repo', 0):.2f} |
| 평균 스타/리포 | {stats.get('avg_stars_per_repo', 0):.2f} |

---

## 💻 언어별 분포

| 언어 | 비율 | 크기 |
|------|------|------|
"""
    
    # 언어 분포
    lang_dist = stats.get('language_distribution', {})
    total_bytes = sum(lang_dist.values())
    
    sorted_langs = sorted(lang_dist.items(), key=lambda x: x[1], reverse=True)[:10]
    for lang, bytes_count in sorted_langs:
        percentage = (bytes_count / total_bytes * 100) if total_bytes > 0 else 0
        mb_size = bytes_count / (1024 * 1024)
        md_content += f"| {lang} | {percentage:.2f}% | {mb_size:.2f} MB |\n"
    
    # 리포지토리 목록
    md_content += "\n---\n\n## 📦 리포지토리 목록\n\n"
    
    sorted_repos = sorted(repos, key=lambda x: x.get('commit_count', 0), reverse=True)
    
    for i, repo in enumerate(sorted_repos[:20], 1):
        md_content += f"""
### {i}. [{repo['name']}]({repo['url']})

**설명**: {repo.get('description', 'No description')}

- **커밋**: {repo.get('commit_count', 0):,}
- **스타**: {repo.get('stars', 0):,}
- **포크**: {repo.get('forks', 0):,}
- **생성일**: {repo.get('created_at', 'N/A')}
- **업데이트**: {repo.get('updated_at', 'N/A')}

"""
        
        # 언어 정보
        if repo.get('languages'):
            langs = ', '.join([f"{k}" for k in list(repo['languages'].keys())[:5]])
            md_content += f"**사용 언어**: {langs}\n\n"
    
    md_content += "\n---\n\n*Generated by GitHub Profile Analyzer*\n"
    
    # 파일 저장
    try:
        with open(output_file, 'w', encoding='utf-8') as f:
            f.write(md_content)
        print(f"✅ Markdown 리포트 생성: {output_file}")
    except Exception as e:
        print(f"❌ Markdown 저장 오류: {e}")


def export_to_csv(data: Dict[str, Any], output_file: str):
    """CSV 형식으로 내보내기 (리포지토리 목록)"""
    import csv
    
    repos = data.get('repositories', [])
    
    try:
        with open(output_file, 'w', newline='', encoding='utf-8') as f:
            fieldnames = ['name', 'url', 'description', 'stars', 'forks', 
                         'commit_count', 'size', 'created_at', 'updated_at']
            
            writer = csv.DictWriter(f, fieldnames=fieldnames)
            writer.writeheader()
            
            for repo in repos:
                writer.writerow({
                    'name': repo.get('name', ''),
                    'url': repo.get('url', ''),
                    'description': repo.get('description', ''),
                    'stars': repo.get('stars', 0),
                    'forks': repo.get('forks', 0),
                    'commit_count': repo.get('commit_count', 0),
                    'size': repo.get('size', 0),
                    'created_at': repo.get('created_at', ''),
                    'updated_at': repo.get('updated_at', '')
                })
        
        print(f"✅ CSV 파일 생성: {output_file}")
        
    except Exception as e:
        print(f"❌ CSV 저장 오류: {e}")


