export function getLanguage(): string {
  const lang = navigator.language || (navigator as any).userLanguage || "en";
  const langLower = lang.toLowerCase();
  if (langLower.startsWith("ko")) return "ko";
  if (langLower.startsWith("ja")) return "ja";
  if (langLower.startsWith("ru")) return "ru";
  if (langLower.startsWith("es")) return "es";
  if (langLower.startsWith("zh-tw") || langLower.startsWith("zh-hk")) return "zh-TW";
  if (langLower.startsWith("zh")) return "zh";
  return "en";
}

export function translate(key: string, currentLang: string): string {
  return i18n[currentLang]?.[key] || i18n["en"]?.[key] || key;
}
